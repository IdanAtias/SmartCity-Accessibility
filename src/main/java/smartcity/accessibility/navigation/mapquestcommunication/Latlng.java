package smartcity.accessibility.navigation.mapquestcommunication;

/**
 * 
 * @author yael This class represent lat and lng of a location.
 */
public class Latlng {
	private Long lat;
	private Long lng;

	public Latlng() {
	}

	public Long getLat() {
		return lat;
	}

	public void setLat(Long lat) {
		this.lat = lat;
	}

	public Long getLng() {
		return lng;
	}

	public void setLng(Long lng) {
		this.lng = lng;
	}

}
